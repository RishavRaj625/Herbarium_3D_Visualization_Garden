  document.getElementById("sendMessage").addEventListener("click", async function() {
    const userMessage = document.getElementById("userMessage").value;
    if (!userMessage) return;
  
    // Add user's message to the chat
    const chatContent = document.getElementById("chatContent");
    const userMessageElement = `<div class="user-message"><strong>You:</strong> ${userMessage}</div>`;
    chatContent.innerHTML += userMessageElement;
    document.getElementById("userMessage").value = "";
  
    // Make request to ChatGPT API
    const response = await fetch('https://api.openai.com/v1/completions', {
      method: 'POST',
      headers: {
        'Content-Type': 'application/json',
        'Authorization': `Bearer YOUR_OPENAI_API_KEY`
      },
      body: JSON.stringify({
        model: "gpt-3.5-turbo",
        messages: [{ role: "system", content: "You are a helpful assistant." }, { role: "user", content: userMessage }],
        max_tokens: 150
      })
    });
  
    const responseData = await response.json();
    const botMessage = responseData.choices[0].message.content;
  
    // Add bot's response to the chat
    const botMessageElement = `<div class="bot-message"><strong>Bot:</strong> ${botMessage}</div>`;
    chatContent.innerHTML += botMessageElement;
  });
  