let currentIndex = 1;
const carouselIntervalTime = 3000; // Time interval in milliseconds (3 seconds)

// Move Carousel Manually
function moveCarousel(step) {
  const carousel = document.querySelector('.carousel__container');
  const items = document.querySelectorAll('.carousel__item');
  const totalItems = items.length;

  currentIndex = (currentIndex + step + totalItems) % totalItems;
  carousel.style.transform = `translateX(-${currentIndex * 100}%)`;
}

// Automatic Carousel Movement
function startCarouselAutoMove() {
  setInterval(() => {
    moveCarousel(1); // Automatically move to the next slide
  }, carouselIntervalTime);
}

// Start automatic carousel movement when the page loads
document.addEventListener('DOMContentLoaded', () => {
  startCarouselAutoMove();
});








